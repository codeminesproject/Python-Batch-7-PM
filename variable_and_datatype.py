
"""
Datatype & Variable

Variable - It is a temporary storage of data which hold data till program execution

Syntax:

variable_name = value

"""

'''
Numeric Data Type:
- Number Without Decimal (int)
- Number With Decimal (float)

Non Numeric Data (string)
All values inside double inverted comma is considred as a string datatype

'''

# string
name = "CodeMines Computer"

# name is a variable and CodeMines Computer is a value of variable name
# value CodeMines Computer is assigned to variable name

# int
age = 19

# value 19 is assigned to variable age
# value 19 is without decimal so datatype of age is int

# float
percentage = 45.67

# value 45.67 is assigned to variable percentage
# value 45.67 is with decimal so datatype of percentage is float


