
# set is unordered

"""
declaration of set:

set_var = {value_1,value_2,value_3,....}

"""

set_var = {10,"Nilima",20,"Aditya","Arman",30,"Veena",40,98.78,True}

print("values of set:",set_var)
print("datatype of set:",type(set_var))
print("no of values in set:",len(set_var))

print("-----------------------------------------------")

# int set

int_set = {10,20,30,40}

print("values of set:",int_set)
