
percentage = input("Please enter percentage: ")

print("value of variable percentage is",percentage)
print("datatype of variable percentage is",type(percentage))

print("-------- convert string into float datatype -------------")

# float(variable) - float is a class used to convert any value of variable in float datatype

flt_percentage = float(percentage)

print("value of variable flt_percentage is",flt_percentage)
print("datatype of variable flt_percentage is",type(flt_percentage))

